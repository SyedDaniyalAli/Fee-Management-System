    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        try {
            int dialogResult=JOptionPane.showConfirmDialog(null,"Are Sure to delete this Record???");
            if(dialogResult == JOptionPane.YES_OPTION)
            {
                int rownum=table.getSelectedRow();
                String value=table.getValueAt(rownum, 0).toString();
                try {
                    
                int result= s.executeUpdate("delete from student where id='"+value+"'");
                if(result>0)
                {
                    JOptionPane.showMessageDialog(this,"Record has been Deleted Succesfully");
                    Refresh();
                }
                } catch (Exception e) {
                    int dialogResults=JOptionPane.showConfirmDialog(this, "Alert if U delete this Record from here their Records will Also Delete from All Places");                    
                    if(dialogResults == JOptionPane.YES_OPTION)
                    {
                        int result1= s.executeUpdate("delete from view_of_Fee where id='"+value+"'");
                        int result2= s.executeUpdate("delete from Fees_Pannel where Fees_Pannel.id='"+value+"'");
                        int result3= s.executeUpdate("delete from student where id='"+value+"'");
                        Refresh();
                        if(result1>0&&result2>0&&result3>0)
                        {
                            JOptionPane.showMessageDialog(this,"Record has been perminently Deleted Succesfully from All places");
                            Refresh();
                        }
                    }
                }
            }
            
  else
 {
   JOptionPane.showMessageDialog(this,"Record has not  been Deleted");
}
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "First Delete This id's Record from Fees Structure");
        }
    }//GEN-LAST:event_jButton5MouseClicked

